package main

import "testing"

var p016Tests = []struct {
	in  int
	out int
}{
	{0, 1},   // 2^0  =     1          1 =  1      =  1
	{1, 2},   // 2^1  =     2          2 =  2      =  2
	{2, 4},   // 2^2  =     4          4 =  4      =  4
	{3, 8},   // 2^3  =     8          8 =  8      =  8
	{4, 7},   // 2^4  =    16,       1+6 =  7      =  7
	{5, 5},   // 2^5  =    32,       3+2 =  5      =  5
	{6, 10},  // 2^6  =    64,       6+4 = 10, 1+0 =  1
	{7, 11},  // 2^7  =   128,     1+2+8 = 11, 1+1 =  2
	{8, 13},  // 2^8  =   256,     2+5+6 = 13, 1+3 =  4
	{9, 8},   // 2^9  =   512,     5+1+2 =  8,   8 =  8
	{10, 7},  // 2^10 =  1024,   1+0+2+4 =  7,   7 =  7
	{11, 14}, // 2^11 =  2048,   2+0+4+8 = 14, 1+4 =  5
	{12, 19}, // 2^12 =  4096,   4+0+9+6 = 19, 1+9 = 10, 1+0 = 1
	{13, 20}, // 2^13 =  8129,   8+1+2+9 = 20, 2+0 =  2
	{14, 22}, // 2^14 = 16384, 1+6+3+8+4 = 22, 2+2 =  4
	{15, 26}, // 2^15 = 32768, 3+2+7+6+8 = 26, 2+6 =  8
	{16, 25}, // 2^16 = 65536, 6+5+5+3+6 = 25, 2+5 =  7
}

func TestP016SumOfPowerDigit(t *testing.T) {
	for _, tt := range p016Tests {
		s := p016SumOfPowerDigit(tt.in)
		if s != tt.out {
			t.Errorf("p016SumOfPowerDigit(%d) => %d, want %d", tt.in, s, tt.out)
		}
	}
}
